// Java program that display the number from 0 to 12 using do wihele loop
public class whilee {
    public static void main(String[] args) {
    // declare variable
        int n=0;
    do{
        System.out.println(n);
        // incrementation
        n++; }
        // Do while loop
        while (n<=12);
    
}

    
}
