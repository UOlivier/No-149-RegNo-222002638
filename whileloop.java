// Java program that displays 10 number for 1
public class whileloop {
    public static void main(String[] args) {  
        // initializing variable
        int i=1; 
        // while loop from 1 to 10 
        while(i<=10){  
            System.out.println(i); 
            // incremantation 
        i++;  
        }  
    }  
    
    
}
